print(list(map(lambda x: x*x, [3,2,6,7,1,4])))
"""
map() permet d'appliquer une fonction à une list

lambda est une fonction anonyme
elle peut prendre un ou plusieur argument et nombre
mais ne contient qu'une expresion

ici on applique la fonction x*x pour chaque élément de la list
donc => 3*3 / 2*2 / 6*6 ...
"""